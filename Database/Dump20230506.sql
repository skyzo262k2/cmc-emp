CREATE DEFINER=`root`@`localhost` PROCEDURE `SP_FindVoitureByEtatFactureAndClient`(Cl varchar(255),tp varchar(255))
begin 

	if Cl = "" and tp = "choisir" then
		select r.*,v.marque,c.nom,f.id_facture,f.Datefacture from Facture f right join 
		Reparation r on f.id_reparation = r.id
		inner join Voiture v using(matricule) inner join Client c on c.id = v.id_Client;
    elseif Cl = "" and tp != "choisir" then
		select r.*,v.marque,c.nom,f.id_facture,f.Datefacture from Facture f right join 
		Reparation r on f.id_reparation = r.id
		inner join Voiture v using(matricule) inner join Client c on c.id = v.id_Client
        where r.statut = tp;
    elseif Cl != "" and tp = "choisir" then
		select r.*,v.marque,c.nom,f.id_facture,f.Datefacture from Facture f right join 
		Reparation r on f.id_reparation = r.id
		inner join Voiture v using(matricule) inner join Client c on c.id = v.id_Client
        where c.nom REGEXP Cl ;
    else
		select r.*,v.marque,c.nom,f.id_facture,f.Datefacture from Facture f right join 
		Reparation r on f.id_reparation = r.id
		inner join Voiture v using(matricule) inner join Client c on c.id = v.id_Client
        where c.nom REGEXP Cl and r.statut = tp;
    end if;
end ;;
